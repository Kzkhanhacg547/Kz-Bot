module.exports.run = async ({ api, event, args }) => {
  if (args.length < 2) {
    return api.sendMessage("Usage: font [fontStyle] [text]", event.threadID);
  }

  const fontStyle = args.shift().toLowerCase();
  const text = args.join(" ");

  let formattedText = text;

  switch (fontStyle) {
    case "serif":
    case "gothic":
    case "bold":
    case "boldsans":
    case "sansserif":
    case "fancy":
    case "vietnamese":
      formattedText = formatFont(fontStyle, text);
      break;
    default:
      return api.sendMessage("Invalid font style. Available styles: serif, gothic, bold, boldsans, sansserif, vietnamese", event.threadID);
  }

  api.sendMessage(formattedText, event.threadID);
};